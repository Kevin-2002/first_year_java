public class JavaSwitch1
{
	public static void main(String[] args)
	{
		//declare variable
		int num1 = 101;

		switch(num1)
		{
			case 100:
				System.out.println("The value of the variable is 100!");
				break;
			default:
				System.out.println("The value is not 100!");
				break;
		}
	}
}