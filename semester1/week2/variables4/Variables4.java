public class Variables4
{
	public static void main(String[] args)
	{
		int n1 = 99;
		int n2 = 51;
		double add = n1 + n2;
		double divide = n1 / n2;
		double multiply = n1 * n2;
		double sub = n1 - n2;
		System.out.println("The variable value of n1 is: " + n1);
		System.out.println("The variable value of n1 is: " + n2);
		System.out.println("n1 plus n2 is: : " + add);
		System.out.println("n1 divide n2 is: : " + divide);
		System.out.println("n1 multiply n2 is: : " + multiply);
		System.out.println("n1 subtract n2 is: : " + sub);
	}
}