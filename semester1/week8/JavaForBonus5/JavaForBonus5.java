public class JavaForBonus5
{
	public static void main(String[] args)
	{
		//for1
		for(int a = 1; a <= 1; a++)
		{
			System.out.println("*");
			//for2
			for(int b = 1; b <= 2; b++)
			{
				System.out.print("*");
				//if
				if(b == 2)
				{
					System.out.print("\n");
				}
				}//for2
				//for3
				for(int c = 1; c <= 3; c++)
				{
					System.out.print("*");
					//if
					if(c == 3)
					{
						System.out.print("\n");
					}
				}//for3
					//for4
					for(int d = 1; d <= 4; d++)
					{
						System.out.print("*");
						//if
						if(d == 4)
						{
							System.out.print("\n");
						}
					}//for4
						//for5
						for(int e = 1; e <= 5; e++)
						{
							System.out.print("*");
							//if
							if(e == 5)
							{
								System.out.print("\n");
							}
						}//for5
							//for6
							for(int f = 1; f <= 6; f++)
							{
								System.out.print("*");
								//if
								if(f == 6)
								{
									System.out.print("\n");
								}
							}//for6
		}//for1
	}
}