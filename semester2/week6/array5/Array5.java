public class Array5
{
	public static void main(String[] args)
	{
		//Create an integer array with 4 elements
		int[] ex5Array = {34, 27, 45, 68, 98};

		//use .length
		System.out.println(ex5Array.length);

		//output to console with for
		for(int i = 0; i  < ex5Array.length; i++)
		{
			System.out.println(ex5Array[i]);
		}
	}
}